import com.ab.hilt_kt.ui.viewmodels.SecondViewModel

class SecondFrag : Fragment() {


    private lateinit var viewModel: SecondViewModel

    override fun onCreateView(
        inflater: LayoutInflater, container: ViewGroup?,
